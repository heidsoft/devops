package com.heidsoft.base.vo;

/*
 * 用户实体类
 */
public class User
{
    String name;
    String pass;
    
    public User(){}
    
    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
    public String getPass()
    {
        return pass;
    }
    public void setPass(String pass)
    {
        this.pass = pass;
    }
    
}
